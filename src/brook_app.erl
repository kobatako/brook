-module(brook_app).

-behaviour(application).


%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
  mnesia:start(),
  Listen = brook_interface:init(),
  brook_arp:init(),
  brook_ip:init(),
  brook_pipeline:init(),
  brook_sup:start_link(Listen).

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

